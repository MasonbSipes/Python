# Python 101 - Data Types #

# Integers
# Integers are whole numbers, positive or negative, without decimals, of unlimited length.
# Example:
apples = 5 # 5 is an integer
bananas = 10 # 10 is an integer
total_fruits = apples + bananas # 5 + 10 = 15
print(total_fruits) # 15
print(type(total_fruits)) # <class 'int'>
print()

# Floats
# Floats are decimal numbers, positive or negative, of unlimited length.
# Example:
bag_of_apples = 5.99 # 5.99 is a float
box_of_cookies = 7.99 # 7.99 is a float
total_cost = bag_of_apples + box_of_cookies # 5.99 + 7.99 = 13.98
print(total_cost) # 13.98
print(type(total_cost)) # <class 'float'>
print()

# Strings
# Strings are a sequence of characters enclosed within single or double quotes.
# Example:
greeting = "Hello"
world_type = ", welcome to the World of Pythonia"
print(greeting + world_type)
print()

# Booleans
# Booleans are binary values: True or False.
total_expenses = 1000
budget = 1050
print(total_expenses > budget) # Flase
